    private void initMyThings(){
        outliersButtonGroup.add(outliers_auto_radiobutton);
        outliersButtonGroup.add(outliers_manually_radiobutton);
        outliersButtonGroup.add(outliers_off_radiobutton);
        outliersButtonGroup.setSelected(outliers_auto_radiobutton.getModel(), true);
        outliers_tolerance_TextField.setText(new Double(Globals.outliersTolerance).toString());
        outliers_lower_bound_textfield.setText(new Double(Globals.outliersLowerBound).toString());
        outliers_upper_bound_textfield.setText(new Double(Globals.outliersUpperBound).toString());
        fileTextField.setText(Globals.pathToDataFile);
        biasCheckbox.setSelected(Globals.removeBias);
        normalizationCheckbox.setSelected(Globals.normalization);
        zoomTextField.setText((new Double(Globals.freqDisplayTrimFactor*100)).toString());
        samplingRateTextField.setText(new Double(Globals.samplingRate).toString());
        this.
        fileButtonActionPerformed(new ActionEvent(this, 0, "First open"));
    }
    
