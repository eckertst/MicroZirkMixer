    private void fileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser(System.getProperty("user.dir"));
        if(Globals.lastFile != null) fileChooser.setCurrentDirectory(Globals.lastFile.getParentFile());
        fileChooser.setMultiSelectionEnabled(false);
        int returnVal = fileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) 
            fileTextField.setText(fileChooser.getSelectedFile().getAbsolutePath());
            Globals.lastFile = fileChooser.getSelectedFile();
    }//GEN-LAST:event_fileButtonActionPerformed

    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        Globals.outliersTolerance = new Double(outliers_tolerance_TextField.getText()).doubleValue();
        Globals.outliersLowerBound = new Double(outliers_lower_bound_textfield.getText()).doubleValue();
        Globals.outliersUpperBound = new Double(outliers_upper_bound_textfield.getText()).doubleValue();
        Globals.pathToDataFile = fileTextField.getText();
        Globals.removeBias = biasCheckbox.isSelected();
        Globals.normalization = normalizationCheckbox.isSelected();
        Globals.freqDisplayTrimFactor = (new Double(zoomTextField.getText()).doubleValue())/100;
        Globals.samplingRate = new Double(samplingRateTextField.getText()).doubleValue();
        Globals.standardZoomFactorTransformed = 100 / new Double(zoomTextField.getText()).doubleValue();
        setVisible(false);
        dispose();
    }//GEN-LAST:event_okButtonActionPerformed

